#Assignment Operators
x=10
print(x)

y=x
print(y)

z=10+2+y
print(z)

z=10+2*y
print(z)

z=(10+2)*y
print(z)

#Multiple Assignment
x,y=10,20
print(x,y)

x,y,z=10,10,10
print(x,y,z)

#Chained Assignment
a=b=c=10
print(a,b,c)

#Compound Assignment(Arthmetic,Simple Assignment)

a=5
a=a+3
print(a)

a+=3 #a=a+3
print(a)

a*=2 #a=a*2
print(a)




















